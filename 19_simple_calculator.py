def add(a, b):
    return a + b

def sub(a, b):
    return a - b

def mul(a, b):
    return a * b

def div(a, b):
    return a / b

a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

print("Add:", add(a, b))
print("Subtract:", sub(a, b))
print("Multiply:", mul(a, b))
print("Divide:", div(a, b))